    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       PreparedStatement ps = null;
        ResultSet rs = null;
       
        try {
            
           
            int Fila = jTable1.getSelectedRow();
            String codigo = jTable1.getValueAt(Fila, 0).toString();

           ps = cn.prepareStatement("SELECT id_cliente, nombre FROM cliente WHERE id_cliente=?");
            //CallableStatement cmd=cn.prepareCall(sql);
            
            ps.setString(1, codigo);
            rs = ps.executeQuery();

            while (rs.next()) {
                jTextField1.setText(rs.getString("id_cliente"));
                jTextField2.setText(rs.getString("nombre"));
                
               
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

